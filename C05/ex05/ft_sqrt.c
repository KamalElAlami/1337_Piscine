int	ft_sqrt(int nb)
{
	long long	i;

	i = 1;
	if (nb <= 0)
	{
		return (0);
	}
	while (i * i < nb)
	{
		++i;
	}
	if (i * i == nb)
	{
		return (i);
	}
	return (0);
}
