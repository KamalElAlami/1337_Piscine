#include <unistd.h>

void	ft_ultimate_div_mod(int *a, int *b)
{
	int	i;
	int	j;

	if (*b != '0')
	{
		i = *a / *b;
		j = *a % *b;
		*a = i;
		*b = j;
	}
}
